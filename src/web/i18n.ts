export type TranslationKey = keyof typeof en;

const dictionaries: Record<UiLanguage, Record<TranslationKey, MessageValue>> = { en, ar };

export function translate(
  language: UiLanguage,
  key: TranslationKey,
  params: Record<string, string | number> = {}
): string {
  const value = dictionaries[language][key] ?? en[key];
  if (typeof value === "function") {
    return value(params);
  }
  return value;
}

export function applyTranslationsToElement(root: ParentNode, language: UiLanguage): void {
  for (const el of Array.from(root.querySelectorAll<HTMLElement>("[data-i18n]"))) {
    const key = el.dataset.i18n as TranslationKey | undefined;
    if (!key) continue;
    el.textContent = translate(language, key);
  }

  for (const el of Array.from(root.querySelectorAll<HTMLElement>("[data-i18n-title]"))) {
    const key = el.dataset.i18nTitle as TranslationKey | undefined;
    if (!key) continue;
    el.title = translate(language, key);
  }

  for (const el of Array.from(root.querySelectorAll<HTMLElement>("[data-i18n-placeholder]"))) {
    const key = el.dataset.i18nPlaceholder as TranslationKey | undefined;
    if (!key) continue;
    if (el instanceof HTMLInputElement || el instanceof HTMLTextAreaElement) {
      el.placeholder = translate(language, key);
    }
  }

  for (const el of Array.from(root.querySelectorAll<HTMLElement>("[data-i18n-aria-label]"))) {
    const key = el.dataset.i18nAriaLabel as TranslationKey | undefined;
    if (!key) continue;
    el.setAttribute("aria-label", translate(language, key));
  }

  for (const el of Array.from(root.querySelectorAll<HTMLElement>("[data-i18n-alt]"))) {
    const key = el.dataset.i18nAlt as TranslationKey | undefined;
    if (!key) continue;
    if (el instanceof HTMLImageElement) {
      el.alt = translate(language, key);
    }
  }
}
